
.MODEL SMALL 
.STACK 100H 

.DATA
 MSG DB  'ENTER THREE CHARACTERS:  $'
 RESULT DB 0AH, 0DH
    A DB ?, 0AH,0DH
    B DB ?, 0AH,0DH  
    C DB ?, '$'
    
 
.CODE
MAIN PROC 
MOV AX,@DATA 
MOV DS,AX


LEA DX,MSG
MOV AH,9
INT 21H 

MOV AH,1
INT 21H
MOV A,AL

MOV AH,1
INT 21H
MOV B,AL

MOV AH,1
INT 21H
MOV C,AL

LEA DX,RESULT
MOV AH,9
INT 21H 

  
MOV AX,4CH
    INT 21H
    MAIN ENDP
END MAIN
